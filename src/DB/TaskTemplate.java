package DB;

public class TaskTemplate {
	
	private int task_tpl_id;
	private String prb_list;
	private String task_tpl_name;
	
	public int getTask_tpl_id() {
		return task_tpl_id;
	}
	public void setTask_tpl_id(int task_tpl_id) {
		this.task_tpl_id = task_tpl_id;
	}
	public String getPrb_list() {
		return prb_list;
	}
	public void setPrb_list(String prb_list) {
		this.prb_list = prb_list;
	}
	public String getTask_tpl_name() {
		return task_tpl_name;
	}
	public void setTask_tpl_name(String task_tpl_name) {
		this.task_tpl_name = task_tpl_name;
	}
	
}
